    public static Vector3 GetCharacterPosition(ShowCharaNumType _showType, int _charaNo, ResultCharacterManager.FaceType _faceType, int _faceNo) {
        switch (_showType) {
            case ShowCharaNumType.Single:
                return characterPosData_PartyMode[_charaNo][0];
            case ShowCharaNumType.Two:
                return characterPosData_Two[_charaNo][0];
            case ShowCharaNumType.Four:
                switch (_faceType) {
                    case ResultCharacterManager.FaceType.Normal:
                        return characterPosData_Four[_charaNo][0];
                    case ResultCharacterManager.FaceType.Happy:
                        return characterPosData_Four[_charaNo][1 + _faceNo];
                    case ResultCharacterManager.FaceType.Sad:
                        return characterPosData_Four[_charaNo][3 + _faceNo];
                    default:
                        return Vector3.zero;
                }
            default:
                return Vector3.zero;
        }
    }
